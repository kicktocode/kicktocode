package javaBasic;

public class RightAngleTrianglePattern {
    public static void main(String[] args) {
        for(int i=0;i<4;i++)
        {
            System.out.print("0");
            for(int j=1;j<=i;j++)
            {
                System.out.print("0");
             }
            System.out.println("\n");
        }


    }
}
