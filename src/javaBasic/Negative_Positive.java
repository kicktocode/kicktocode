package javaBasic;

public class Negative_Positive
{
    public static void main(String[] args)
    {
        int n;
        n=-56;
        if(n>0)
        {
            System.out.println("Entered integer is positive");
        }
        else if(n<0)
        {
            System.out.println("Entered number is negative");
        }
        else
        {
            System.out.println("Entered number is zero");
        }
    }

}
